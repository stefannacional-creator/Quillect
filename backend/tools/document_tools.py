# ---------------------------------------------------------------------------
# Tool executor
# ---------------------------------------------------------------------------


def execute_tool(
    name: str,
    args: dict[str, Any],
    builder: DocumentBuilder,
) -> tuple[str, dict | None]:
    """
    Run a named tool against *builder*.
    Returns (result_string_for_llm, optional_sse_event_dict).
    """
    if name == "plan_document_outline":
        result = builder.plan_outline(
            title=args.get("title", builder.title),
            document_type=args.get("document_type", builder.document_type),
            sections=args.get("sections", []),
        )
        event = {
            "type": "outline_planned",
            "sections": [
                {"id": s["id"], "heading": s["heading"]}
                for s in args.get("sections", [])
            ],
        }
        return result, event

    elif name == "write_section":
        result = builder.write_section(
            section_id=args.get("section_id", ""),
            heading=args.get("heading", ""),
            content=args.get("content", ""),
            level=args.get("level", 2),
        )
        event = {
            "type": "section_written",
            "section_id": args.get("section_id"),
            "heading": args.get("heading"),
        }
        return result, event

    elif name == "finalize_document":
        result = builder.finalize()
        return result, None

    else:
        return f"Unknown tool: {name}", None
